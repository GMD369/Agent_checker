# string checker
def is_string(value):
    return isinstance(value, str)

# integer checker
def is_integer(value):
    return isinstance(value, int)
# float checker
def is_float(value):
    return isinstance(value, float)

# list checker
def is_list(value):
    return isinstance(value, list)

# dictionary checker
def is_dictionary(value):
    return isinstance(value, dict)